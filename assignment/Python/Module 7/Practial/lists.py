# Create a list with elements of multiple data types
mixed_list = [42, "Hello, world!", 3.14, True, None, [1, 2, 3], {"key": "value"}, (10, 20)]

# Print the list
print("Mixed Data Type List:")
print(mixed_list)

# Accessing individual elements
print("\nAccessing individual elements:")
print(f"Integer: {mixed_list[0]}")
print(f"String: {mixed_list[1]}")
print(f"Float: {mixed_list[2]}")
print(f"Boolean: {mixed_list[3]}")
print(f"NoneType: {mixed_list[4]}")
print(f"Nested List: {mixed_list[5]}")
print(f"Dictionary: {mixed_list[6]}")
print(f"Tuple: {mixed_list[7]}")
