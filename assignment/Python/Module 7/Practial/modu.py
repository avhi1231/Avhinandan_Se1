# Importing the math module
import math

# Define some sample values
number1 = 16
number2 = 5.7
number3 = -4.3

# Using sqrt() to find the square root
square_root = math.sqrt(number1)
print(f"The square root of {number1} is: {square_root}")

# Using ceil() to round up to the nearest integer
ceiling_value = math.ceil(number2)
print(f"The ceiling value of {number2} is: {ceiling_value}")

# Using floor() to round down to the nearest integer
floor_value = math.floor(number3)
print(f"The floor value of {number3} is: {floor_value}")
