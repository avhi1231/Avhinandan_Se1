# Creating a dictionary
my_dict = {
    "name": "Abhi",
    "age": 30,
    "city": "Patna",
    "profession": "Engineer",
    "hobbies": ["reading", "traveling", "coding"],
    "is_student": False
}

# Accessing values using keys
name = my_dict["name"]
age = my_dict["age"]
city = my_dict["city"]
profession = my_dict["profession"]
hobbies = my_dict["hobbies"]
is_student = my_dict["is_student"]

# Printing the values
print("Name:", name)
print("Age:", age)
print("City:", city)
print("Profession:", profession)
print("Hobbies:", hobbies)
print("Is Student:", is_student)