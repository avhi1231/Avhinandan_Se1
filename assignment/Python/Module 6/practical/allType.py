# Integer
age = 30

# Float
height = 5.8

# String
name = "Alice"

# List
hobbies = ["reading", "traveling", "coding"]

# Tuple
coordinates = (10.5, 20.5)

# Dictionary
person_info = {"name": "Alice", "age": 30, "height": 5.8}

# Set
unique_numbers = {1, 2, 3, 4, 5}

# Print all
print(f"Age (int): {age}")
print(f"Height (float): {height}")
print(f"Name (str): {name}")
print(f"Hobbies (list): {hobbies}")
print(f"Coordinates (tuple): {coordinates}")
print(f"Person Info (dict): {person_info}")
print(f"Unique Numbers (set): {unique_numbers}")
