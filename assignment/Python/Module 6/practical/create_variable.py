# Integer variable
age = 30

# Float variable
height = 5.8

# String variable
name = "Alice"

# Boolean variable
is_active = True

# List variable
hobbies = ["reading", "swimming", "coding"]

# Tuple variable
coordinates = (10, 20)

# Dictionary variable
user_info = {"name": "Alice", "age": 30}

# Printing variables
print(f"Name: {name}, Age: {age}, Height: {height}, Active: {is_active}")
print(f"Hobbies: {hobbies}")
print(f"Coordinates: {coordinates}")
print(f"User Info: {user_info}")
